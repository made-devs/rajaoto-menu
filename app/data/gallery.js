// Helper functions
export const getImagesByCategory = (categorySlug) => {
  if (categorySlug === 'all') return galleryImages;
  return galleryImages.filter((image) => image.category === categorySlug);
};

export const getImageById = (id) => {
  return galleryImages.find((image) => image.id === id);
};
